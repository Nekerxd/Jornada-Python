nome = "Guido"
sobrenome = "Van Rossum"
nome_completo = nome + " " + sobrenome

#Concatenação
print(nome + " " + sobrenome)

#Transformar em minúsculas
print(nome_completo.lower())

#Transformar em maiúsculas
print(nome_completo.upper())

#Apenas as iniciais maiúsculas
print(nome_completo.capitalize())

#Remoção de espaços no início e fim
print("      Guido Van Rossum      ".strip())

#Quebra de linha
print("Exemplo de \n newline")

#Tabulação
print("Exemplo de \t tabulação")
